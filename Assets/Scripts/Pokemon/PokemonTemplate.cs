    public static float GetEffectiveness(PokemonType attackType, PokemonType defenseType){
        if(attackType == PokemonType.None || defenseType == PokemonType.None){
            return 1;
        }
        // make sure Pokemon Type is in the same order as Pokemon Chart
        int row = (int)attackType -1; // subtract one since none is = 0 and chart starts at 0
        int col = (int)defenseType -1; // eg normal =1 in enum but needs to be at 0 for chart
        return chart[row][col];
    }    

